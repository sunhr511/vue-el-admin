<template>
  <div class="login-container">
    <el-form class="login-form" autoComplete="on" :model="loginForm" :rules="loginRules" ref="loginForm" label-position="left">
      <h3 class="title">{{sysconfig.title + '管理系统'}}</h3>
      <el-form-item prop="username">
        <span class="svg-container svg-container_login">
          <svg-icon icon-class="user" />
        </span>
        <el-input name="username" id="username" type="text" v-model="loginForm.username" autoComplete="on" placeholder="请输入帐号" />
      </el-form-item>
      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password"></svg-icon>
        </span>
        <el-input name="password" id="password" :type="pwdType" @keyup.enter.native="handleLogin" v-model="loginForm.password" autoComplete="on"
          placeholder="请输入密码"></el-input>
          <span class="show-pwd" @click="showPwd"><svg-icon :icon-class="pwdType ? 'close-eye' : 'open-eye'" /></span>
      </el-form-item>
      <el-form-item>
        <el-button type="primary" style="width:100%;" :loading="loading" @click.native.prevent="handleLogin">
          登  录
        </el-button>
      </el-form-item>
    </el-form>
  </div>
</template>

<script>
import { encodePCInfor } from '@/api/common'
import { validateUsername, validatePass } from '@/utils/validate'
import sysconfig from '@/sysconfig'
import Cookies from 'js-cookie'

export default {
  name: 'login',
  data() {
    return {
      loginForm: {
        username: '',
        password: ''
      },
      loginRules: {
        username: [{ required: true, trigger: 'blur', validator: validateUsername }],
        password: [{ required: true, trigger: 'blur', validator: validatePass }]
      },
      loading: false,
      pwdType: 'password'
    }
  },
  created() {
    this.sysconfig = sysconfig
  },
  methods: {
    showPwd() {
      if (this.pwdType === 'password') {
        this.pwdType = ''
      } else {
        this.pwdType = 'password'
      }
    },
    handleLogin() {
      document.getElementById('username').blur()
      document.getElementById('password').blur()
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          this.$store.dispatch('Login', this.loginForm).then((res) => {
            this.loading = false
            encodePCInfor(this.loginForm).then(res => {
              Cookies.set('encryptU', res.data.username)
              Cookies.set('encryptP', res.data.password)
              this.$router.push({ path: '/' })
            })
          }).catch((err) => {
            console.log(err)
            this.loading = false
          })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    }
  }
}
</script>

